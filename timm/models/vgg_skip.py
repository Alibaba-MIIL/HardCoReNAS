import torch
import torch.nn as nn
from .registry import register_model

__all__ = [
    'VGG', 'vgg11_skip', 'vgg11_bn_skip', 'vgg13_skip', 'vgg13_bn_skip', 'vgg16_skip', 'vgg16_bn_skip',
    'vgg19_bn_skip', 'vgg19_skip',
]


class VGG(nn.Module):

    def __init__(self, features, num_classes=1000, init_weights=True, **kwargs):
        super(VGG, self).__init__()
        self.features = features
        self.avgpool = nn.AdaptiveAvgPool2d((7, 7))
        self.classifier = nn.Sequential(
            nn.Linear(512 * 7 * 7, 4096),
            nn.ReLU(True),
            nn.Dropout(),
            nn.Linear(4096, 4096),
            nn.ReLU(True),
            nn.Dropout(),
            nn.Linear(4096, num_classes),
        )
        if init_weights:
            self._initialize_weights()

    def forward(self, x):
        x = self.features(x)
        x = self.avgpool(x)
        x = torch.flatten(x, 1)
        x = self.classifier(x)
        return x

    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.BatchNorm2d):
                nn.init.constant_(m.weight, 1)
                nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.Linear):
                nn.init.normal_(m.weight, 0, 0.01)
                nn.init.constant_(m.bias, 0)


def make_layers(cfg, batch_norm=False, no_skip=False):
    layers = []
    in_channels = 3
    for v in cfg:
        if v == 'M':
            layers += [nn.MaxPool2d(kernel_size=2, stride=2)]
        else:
            if batch_norm:
                layers += [base_module_BN(in_channels, v, no_skip)]
            else:
                layers += [base_module(in_channels, v, no_skip)]
            in_channels = v
    return nn.Sequential(*layers)


class base_module_BN(nn.Module):
    def __init__(self, in_channels, v, no_skip):
        super(base_module_BN, self).__init__()

        self.conv2d = nn.Conv2d(in_channels, v, kernel_size=3, padding=1)
        self.bn = nn.BatchNorm2d(v)
        self.relu = nn.ReLU(inplace=True)
        self.in_channels = in_channels
        self.v = v
        self.no_skip = no_skip

    def forward(self, x):
        if self.in_channels == self.v and not self.no_skip:
            return self.relu(self.bn(self.conv2d(x)) + x)
        else:
            return self.relu(self.bn(self.conv2d(x)))


class base_module(nn.Module):
    def __init__(self, in_channels, v, no_skip):
        super(base_module, self).__init__()
        self.conv2d = nn.Conv2d(in_channels, v, kernel_size=3, padding=1)
        self.relu = nn.ReLU(inplace=True)
        self.in_channels = in_channels
        self.v = v
        self.no_skip = no_skip

    def forward(self, x):
        if self.in_channels == self.v and not self.no_skip:
            return self.relu(self.conv2d(x) + x)
        else:
            return self.relu(self.conv2d(x))


cfgs = {
    'A': [64, 'M', 128, 'M', 256, 256, 'M', 512, 512, 'M', 512, 512, 'M'],
    'B': [64, 64, 'M', 128, 128, 'M', 256, 256, 'M', 512, 512, 'M', 512, 512, 'M'],
    'D': [64, 64, 'M', 128, 128, 'M', 256, 256, 256, 'M', 512, 512, 512, 'M', 512, 512, 512, 'M'],
    'E': [64, 64, 'M', 128, 128, 'M', 256, 256, 256, 256, 'M', 512, 512, 512, 512, 'M', 512, 512, 512, 512, 'M'],
}


def _vgg(arch, cfg, batch_norm, pretrained, progress, no_skip=False, **kwargs):
    if pretrained:
        kwargs['init_weights'] = False
    model = VGG(make_layers(cfgs[cfg], batch_norm=batch_norm, no_skip=no_skip), **kwargs)
    return model


@register_model
def vgg11_skip(pretrained=False, progress=True, **kwargs):
    r"""VGG 11-layer model (configuration "A") from
    `"Very Deep Convolutional Networks For Large-Scale Image Recognition" <https://arxiv.org/pdf/1409.1556.pdf>`_

    Args:
        pretrained (bool): If True, returns a model pre-trained on ImageNet
        progress (bool): If True, displays a progress bar of the download to stderr
    """
    return _vgg('vgg11', 'A', False, pretrained, progress, **kwargs)


